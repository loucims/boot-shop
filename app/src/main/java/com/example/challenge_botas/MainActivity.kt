@Composable
fun AppNavigation() {
    val navController = rememberNavController()

    NavHost(navController = navController, startDestination = Routes.BootsListScreen) {
        composable(Routes.BootsListScreen) {
            BootsListScreen(onBuyClick = {
                navController.navigate(Routes.SelectSizeScreen)
            })
        }
        composable(Routes.SelectSizeScreen) {
            SelectSizeScreen(
                onBack = { navController.popBackStack() },
                onBuy = { /* Handle buy action */ }
            )
        }
    }
}

